/**
 * AILogo — inline SVG logos for the 4 tracked AI models + Grok.
 * Props:
 *   model — model identifier string (case-insensitive matching)
 *   className — forwarded to the <svg> element
 */

type Props = { model: string; className?: string };

export default function AILogo({ model, className = "w-4 h-4" }: Props) {
  const id = model.toLowerCase();

  if (id.includes("gpt") || id.includes("chatgpt") || id.includes("openai")) {
    return <ChatGPTIcon className={className} />;
  }
  if (id.includes("claude") || id.includes("anthropic")) {
    return <ClaudeIcon className={className} />;
  }
  if (id.includes("gemini") || id.includes("google")) {
    return <GeminiIcon className={className} />;
  }
  if (id.includes("perplexity")) {
    return <PerplexityIcon className={className} />;
  }
  if (id.includes("grok") || id.includes("xai")) {
    return <GrokIcon className={className} />;
  }

  // fallback — generic sparkle
  return (
    <svg className={className} viewBox="0 0 24 24" fill="currentColor">
      <path d="M12 2l2.09 6.26L20 10l-5.91 1.74L12 18l-2.09-6.26L4 10l5.91-1.74z" />
    </svg>
  );
}

